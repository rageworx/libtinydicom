int32_t DicomDictionary::FindKeyIndex( const uint32_t id )
{
    int     nCnt = 0;

    if( id == 0x00000000 )
    {
        return -1;
    }

    uint16_t remasked = ( id >> 16 ) & 0xFF00;
    uint32_t rem_id   = id;

    if ( ( remasked == 0x5000 ) || ( remasked == 0x6000 ) )
    {
        rem_id = ( remasked << 16 ) | 0x00FF0000;
    }

    while ( dicom_dict[nCnt].id != 0x00000000 )
    {
        if ( dicom_dict[nCnt].id == rem_id )
            return nCnt;

        nCnt++;
    }

    return -1;
}

int32_t DicomDictionary::FindKeyIndexFrom(const uint32_t id, uint32_t idx )
{
    if( idx >= (int)sizeof( dicom_dict ) )
        return -2;

    int nCnt = idx;

    if( id == 0x00000000 )
    {
        return -1;
    }

    while( dicom_dict[nCnt].id != 0 )
    {
        if( dicom_dict[nCnt].id == id )
            return nCnt;

        nCnt++;
    }

    return -1;
}

uint16_t  DicomDictionary::GetVR(const uint32_t id, bool* sameavailed )
{
    uint16_t   nRet    = 0;
    int    nIndex  = FindKeyIndex(id);

    if( nIndex >= 0 )
    {
        if ( strlen( dicom_dict[nIndex].vr ) > 0 )
        {
            memcpy( &nRet, dicom_dict[nIndex].vr, 2 );

            if ( sameavailed != NULL)
            {
                if ( FindKeyIndexFrom( id, nIndex ) > 0 )
                {
                    *sameavailed = true;
                }
                else
                {
                    *sameavailed = false;
                }
            }
        }
    }

    return nRet;
}

void DicomDictionary::GetVR(const uint32_t id, void *pVR, bool* sameavailed)
{
    bool bRet = false;
    uint16_t nRet = GetVR( id, &bRet );

    if( pVR != NULL )
    {
        memcpy( pVR, &nRet, 2 );

        if ( sameavailed != NULL)
        {
            *sameavailed = bRet;
        }
    }
}

int32_t DicomDictionary::GetElemState( const uint32_t id )
{
    int32_t nIndex = FindKeyIndex(id);

    if( nIndex >= 0 )
    {
        return dicom_dict[nIndex].state;
    }

    return ELEM_STATE_UNKNWON;
}

int DicomDictionary::GetVRarray( const uint32_t id, uint16_t**pVRarray )
{
    if ( *pVRarray != NULL )
        return -1;

    int nIndex = FindKeyIndex(id);
    int nCount = 0;

    while ( nIndex > 0 )
    {
        *pVRarray = new uint16_t;
        if ( *pVRarray != NULL )
        {
            if ( strlen ( dicom_dict[nIndex].vr ) > 0 )
            {
                memcpy( *pVRarray, dicom_dict[nIndex].vr, 2 );
                nCount++;

                nIndex = FindKeyIndexFrom( id, nIndex );

                if ( nIndex > 0 )
                {
                    pVRarray++;
                }
            }
        }
    }

    return nCount;
}

const char* DicomDictionary::GetMean(const uint32_t id)
{
    int32_t nIndex = FindKeyIndex( id );

    if( nIndex >= 0 )
    {
        return dicom_dict[nIndex].mean;
    }

    return NULL;
}
